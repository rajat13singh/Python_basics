# Relational Operators in Python
# Used to compare two values → result is True or False

a = 10
b = 5

# > (Greater than)
print(a > b)    # True

# < (Less than)
print(a < b)    # False

# >= (Greater than or equal to)
print(a >= b)   # True

# <= (Less than or equal to)
print(a <= b)   # False

# == (Equal to)
print(a == b)   # False

# != (Not equal to)
print(a != b)   # True


# Using different values
x = 7
y = 7

print(x == y)   # True
print(x != y)   # False
print(x >= y)   # True
print(x <= y)   # True


# Important notes
# == checks value equality
# = is assignment (not comparison)
# Output is always True or False