# Arithmetic Operators in Python

# + (Addition)
a = 10
b = 3
print(a + b)   # 13

# - (Subtraction)
print(a - b)   # 7

# * (Multiplication)
print(a * b)   # 30

# / (Division - returns float)
print(a / b)   # 3.333...

# // (Floor Division - removes decimal)
print(a // b)  # 3 its quotient

# % (Modulus - remainder)
print(a % b)   # 1

# ** (Exponent - power)
print(a ** b)  # 1000


# Using different values
x = 5
y = 2

print(x + y)   # 7
print(x - y)   # 3
print(x * y)   # 10
print(x / y)   # 2.5
print(x // y)  # 2
print(x % y)   # 1
print(x ** y)  # 25


# Important notes
# / always gives float
# // gives integer (floor value)
# % gives remainder
# ** means power