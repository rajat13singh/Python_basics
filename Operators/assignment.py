# Assignment Operators in Python

# = (Simple Assignment)
a = 10
print(a)   # 10

# += (Add and assign)
a += 5     # a = a + 5
print(a)   # 15

# -= (Subtract and assign)
a -= 3     # a = a - 3
print(a)   # 12

# *= (Multiply and assign)
a *= 2     # a = a * 2
print(a)   # 24

# /= (Divide and assign)
a /= 4     # a = a / 4
print(a)   # 6.0

# //= (Floor divide and assign)
a //= 2    # a = a // 2
print(a)   # 3.0

# %= (Modulus and assign)
a %= 2     # a = a % 2
print(a)   # 1.0

# **= (Power and assign)
a = 2
a **= 3    # a = a ** 3
print(a)   # 8


# Important notes
# +=, -=, *= etc are shorthand operators
# a += 5 means a = a + 5
# /= always gives float result